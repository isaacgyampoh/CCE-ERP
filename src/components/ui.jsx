/* ScoreBadge */
export const ScoreBadge = ({ score }) => {
  const dot = score >= 65 ? 'var(--ok)' : score >= 35 ? 'var(--warn)' : 'var(--muted)'
  const label = score >= 65 ? 'Hot' : score >= 35 ? 'Warm' : 'Cold'
  return (
    <span className="badge" title={`Lead score: ${score}/100`}>
      <span className="dot" style={{ background: dot, width:7, height:7, borderRadius:'50%', display:'inline-block' }}/>
      {score} · {label}
    </span>
  )
}

/* ProgressBar */
export const ProgressBar = ({ value, max }) => {
  const pct = max ? Math.round((value / max) * 100) : 0
  return (
    <div style={{ flex:1, height:4, background:'var(--border)', borderRadius:2, overflow:'hidden' }}>
      <div style={{ height:'100%', borderRadius:2, background:'var(--accent)', width:`${Math.min(pct,100)}%`, transition:'width .2s' }}/>
    </div>
  )
}

