import { STATUS } from '@/lib/constants'

/* Avatar — gray bg, up to 2 initials */
export const Avatar = ({ name = '', size = 32, className = '' }) => {
  const parts = name.trim().split(' ')
  const initials = parts.length > 1
    ? (parts[0][0] + parts[parts.length - 1][0]).toUpperCase()
    : (parts[0]?.[0] || '?').toUpperCase()
  return (
    <div
      style={{ width: size, height: size, minWidth: size, fontSize: size * 0.34, background: '#ededf0', color: 'var(--ink-2)', borderRadius: '50%', display: 'flex', alignItems: 'center', justifyContent: 'center', fontWeight: 600, flexShrink: 0 }}
      className={className}
    >
      {initials}
    </div>
  )
}

/* Badge — dot + label, matches design system */
export const Badge = ({ status, className = '' }) => {
  const s = STATUS[status] || { label: status || '—', dot: 'var(--muted)' }
  return (
    <span className={`badge ${className}`}>
      <span className="dot" style={{ background: s.dot, width: 7, height: 7, borderRadius: '50%', flexShrink: 0, display: 'inline-block' }}/>
      {s.label}
    </span>
  )
}

/* Modal */
export const Modal = ({ title, onClose, children, maxWidth = 'max-w-md' }) => (
  <div className="modal-backdrop" onClick={e => e.target === e.currentTarget && onClose()}>
    <div className={`modal ${maxWidth} w-full`}>
      <div style={{ display:'flex', alignItems:'center', justifyContent:'space-between', padding:'12px 16px', borderBottom:'1px solid var(--border)' }}>
        <span style={{ fontWeight:600, fontSize:13, color:'var(--ink)' }}>{title}</span>
        <button onClick={onClose} style={{ color:'var(--ink-3)', background:'none', border:'none', cursor:'pointer', lineHeight:1 }}>
          <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
            <line x1="18" y1="6" x2="6" y2="18"/><line x1="6" y1="6" x2="18" y2="18"/>
          </svg>
        </button>
      </div>
      <div style={{ padding:'16px' }}>{children}</div>
    </div>
  </div>
)

/* KpiStrip + Kpi — single bordered panel with dividers */
export const KpiStrip = ({ children, cols }) => (
  <div className="kpis" style={cols ? { '--kpi-cols': cols } : {}}>
    {children}
  </div>
)

export const Kpi = ({ label, value, delta, up, down }) => (
  <div className="kpi">
    <div className="kpi-label">{label}</div>
    <div className="kpi-val mono">{value}</div>
    {delta && (
      <div className="kpi-delta">
        {up   && <span className="up">{up}</span>}
        {down && <span className="down">{down}</span>}
        {' '}{delta}
      </div>
    )}
  </div>
)

